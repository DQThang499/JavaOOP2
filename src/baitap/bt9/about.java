package baitap.bt9;

public class about {
    protected String hoten;
    protected String diachi;
    public about (String hoten, String diachi){
        this.hoten = hoten;
        this.diachi = diachi;
    }

    public String getHoten() {
        return hoten;
    }

    public void setHoten(String hoten) {
        this.hoten = hoten;
    }
    
    public String getDiachi() {
        return diachi;
    }

    public void setDiachi(String diachi) {
        this.diachi = diachi;
    }
}
